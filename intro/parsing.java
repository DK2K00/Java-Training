package intro;

public class parsing {
    public static void main(String[] args) {
        int n = Integer.parseInt("20");
        n+=1;
        System.out.println(n);

        String s = "2019";
        s+=1;
        System.out.println(s);

        double num = Double.parseDouble("23.45");
        num+=1.55;
        System.out.println(num);

        String str = "25.67";
        str+=1;
        System.out.println(str);
    }
}
